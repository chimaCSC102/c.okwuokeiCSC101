fn main() {
    //addition
    let sum = 5550 + 7310;
    println!("The sum of the two numbers is {}", sum );

    //subtraction
    let difference:f32 = 95.5 - 4.3;
    println!("The difference of the two numbers is {}",difference );

    //multiplication
    let product:i32 = 4 * 30;
    println!("The product of the two numbers is {}", product );

    //division
    let quotient = 56.7 / 32.2;
    println!("The division of the two numbers is {}", quotient );

    //remainder
    let remainder = 43 % 5;
    println!("The remainder of the two numbers are {}", remainder );
}
