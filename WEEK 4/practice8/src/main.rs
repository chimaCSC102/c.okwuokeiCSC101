fn main() {
    let a = 10;
    let b = 2;
    let mut result:i32;

    result = a + b;
    println!("Sum: {}",result );


    result = a - b;
    println!("Difference: {}",result );
    

    result = a * b;
    println!("Product: {}",result );
    

    result = a / b;
    println!("Quotient: {}",result );
    

    result = a % b;
    println!("Remainder: {}",result );
    

}
